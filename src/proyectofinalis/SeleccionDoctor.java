    void cargarLista(){
        List<Doctor> lista = ser.login("http://localhost:8084/EasyNutritionService/webresources/Nutrition/getDoctores");
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.setColumnIdentifiers(new Object[]{"Id", "Nombre", "Apellidos"});
        int con= 0;
        try{
            for(Doctor r:lista){
                Vector v = new Vector();
                v.add(r.getIdDoctor());
                v.add(r.getNombre());
                v.add(r.getApellidos());
                modelo.addRow(v);
            }
            doctores.setModel(modelo);
            
        }catch(Exception e){
            System.out.println(e);
        }       
    }
    
