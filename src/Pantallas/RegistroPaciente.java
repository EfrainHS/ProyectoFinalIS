    private void btn_registrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_registrarActionPerformed
        Servicio ser = new Servicio();
        
        String dia= Integer.toString(txt_fechanaci.getCalendar().get(Calendar.DAY_OF_MONTH));
        String mes= Integer.toString(txt_fechanaci.getCalendar().get(Calendar.MONTH)+1);
        String anio= Integer.toString(txt_fechanaci.getCalendar().get(Calendar.YEAR));
        String fecha = (anio + "-" + mes + "-" + dia);
        
        String a = txt_nombre.getText();
        String b = txt_apellidos.getText();
        String c = txt_genero.getSelectedItem().toString();
        String d = txt_iddoctor.getText();
        String e = txt_domicilio.getText();
        String f = txt_correo.getText();
        String g = txt_contra.getText();
        String h = txt_telefono.getText();
        String i = estatuss;
        
        String parametros = "nombre="+a+"&apellidos="+b+"&domicilio="+e+"&fecha="+fecha+"&genero="+c+"&correo="+f+"&password="+g+"&telefono="+h+"&estatus="+i+"&idDoctor="+d;
        ser.registrar("http://localhost:8084/EasyNutritionService/webresources/Nutrition/registroPaciente", parametros);
        
        JOptionPane.showMessageDialog(null, "La informacion del cliente ha sido guardada exitosamente!");
        
        AdminPaciente ap = new AdminPaciente();
        ap.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_btn_registrarActionPerformed

